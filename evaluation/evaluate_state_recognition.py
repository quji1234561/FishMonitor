#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Evaluate the rule-based fish activity state recognizer.

This script treats the activity labels generated from the full sequence as the
reference labels, learns speed thresholds only from the temporal train split,
and evaluates on val/test frames. It is meant to provide a stable, report-ready
metric for the state-recognition module.
"""

import argparse
from pathlib import Path

import numpy as np
import pandas as pd
from PIL import Image, ImageDraw


STATE_NAMES = {
    0: "low_activity",
    1: "normal_activity",
    2: "high_activity",
}


def parse_args():
    parser = argparse.ArgumentParser(description="Evaluate activity state recognition accuracy")
    parser.add_argument(
        "--activity-csv",
        default="output/activity/BT-001/auto_speed_labels_all_frames.csv",
        help="CSV generated by scripts/auto_label_fish_activity.py",
    )
    parser.add_argument("--out-dir", default="output/evaluation/state_recognition")
    parser.add_argument("--train-ratio", type=float, default=0.7)
    parser.add_argument("--val-ratio", type=float, default=0.2)
    parser.add_argument("--low-percentile", type=float, default=33.0)
    parser.add_argument("--high-percentile", type=float, default=67.0)
    return parser.parse_args()


def split_frames(frames, train_ratio: float, val_ratio: float):
    frames = sorted(int(f) for f in frames)
    n = len(frames)
    n_train = int(n * train_ratio)
    n_val = int(n * val_ratio)
    return {
        "train": set(frames[:n_train]),
        "val": set(frames[n_train : n_train + n_val]),
        "test": set(frames[n_train + n_val :]),
    }


def predict_state(speed_values, low_thr: float, high_thr: float):
    speed_values = np.asarray(speed_values, dtype=float)
    return np.where(speed_values <= low_thr, 0, np.where(speed_values >= high_thr, 2, 1))


def confusion_matrix(y_true, y_pred, n_classes: int = 3):
    cm = np.zeros((n_classes, n_classes), dtype=int)
    for t, p in zip(y_true, y_pred):
        cm[int(t), int(p)] += 1
    return cm


def metrics_from_cm(cm):
    rows = []
    total = int(cm.sum())
    correct = int(np.trace(cm))
    overall_acc = correct / total if total else 0.0

    precisions = []
    recalls = []
    f1s = []

    for sid in range(cm.shape[0]):
        tp = int(cm[sid, sid])
        fp = int(cm[:, sid].sum() - tp)
        fn = int(cm[sid, :].sum() - tp)
        support = int(cm[sid, :].sum())
        precision = tp / (tp + fp) if tp + fp else 0.0
        recall = tp / (tp + fn) if tp + fn else 0.0
        f1 = 2 * precision * recall / (precision + recall) if precision + recall else 0.0
        precisions.append(precision)
        recalls.append(recall)
        f1s.append(f1)
        rows.append(
            {
                "state_id": sid,
                "state_name": STATE_NAMES[sid],
                "support": support,
                "precision": precision,
                "recall": recall,
                "f1": f1,
            }
        )

    summary = {
        "accuracy": overall_acc,
        "macro_precision": float(np.mean(precisions)),
        "macro_recall": float(np.mean(recalls)),
        "macro_f1": float(np.mean(f1s)),
        "support": total,
    }
    return summary, rows


def save_confusion_png(cm, out_path: Path):
    cell = 130
    margin_left = 190
    margin_top = 150
    width = margin_left + cell * 3 + 40
    height = margin_top + cell * 3 + 70
    img = Image.new("RGB", (width, height), "white")
    draw = ImageDraw.Draw(img)

    draw.text((20, 20), "State Recognition Confusion Matrix", fill=(20, 20, 20))
    draw.text((margin_left + 70, 55), "Predicted", fill=(20, 20, 20))
    draw.text((25, margin_top + 150), "True", fill=(20, 20, 20))

    max_value = max(int(cm.max()), 1)
    labels = ["low", "normal", "high"]

    for i, label in enumerate(labels):
        draw.text((margin_left + i * cell + 30, margin_top - 35), label, fill=(20, 20, 20))
        draw.text((margin_left - 120, margin_top + i * cell + 55), label, fill=(20, 20, 20))

    for r in range(3):
        for c in range(3):
            value = int(cm[r, c])
            intensity = int(245 - 180 * (value / max_value))
            color = (intensity, intensity + 5 if intensity < 245 else 245, 255)
            x0 = margin_left + c * cell
            y0 = margin_top + r * cell
            draw.rectangle((x0, y0, x0 + cell, y0 + cell), fill=color, outline=(80, 80, 80))
            draw.text((x0 + 45, y0 + 55), str(value), fill=(20, 20, 20))

    out_path.parent.mkdir(parents=True, exist_ok=True)
    img.save(out_path)


def main():
    args = parse_args()
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(args.activity_csv)
    required = {"frame", "speed_smooth", "state_id"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"activity CSV missing columns: {sorted(missing)}")

    df = df.copy()
    df["frame"] = df["frame"].astype(int)
    df["state_id"] = df["state_id"].astype(int)
    splits = split_frames(df["frame"].unique(), args.train_ratio, args.val_ratio)

    train = df[df["frame"].isin(splits["train"])]
    low_thr = float(np.percentile(train["speed_smooth"], args.low_percentile))
    high_thr = float(np.percentile(train["speed_smooth"], args.high_percentile))

    split_summaries = []
    all_metric_rows = []
    for split_name in ["train", "val", "test"]:
        sub = df[df["frame"].isin(splits[split_name])].copy()
        pred = predict_state(sub["speed_smooth"], low_thr, high_thr)
        cm = confusion_matrix(sub["state_id"].to_numpy(), pred)
        summary, rows = metrics_from_cm(cm)

        split_summaries.append({"split": split_name, **summary})
        for row in rows:
            all_metric_rows.append({"split": split_name, **row})

        pd.DataFrame(cm, index=STATE_NAMES.values(), columns=STATE_NAMES.values()).to_csv(
            out_dir / f"{split_name}_confusion_matrix.csv",
            encoding="utf-8-sig",
        )
        save_confusion_png(cm, out_dir / f"{split_name}_confusion_matrix.png")

    pd.DataFrame(split_summaries).to_csv(out_dir / "overall_metrics.csv", index=False, encoding="utf-8-sig")
    pd.DataFrame(all_metric_rows).to_csv(out_dir / "per_class_metrics.csv", index=False, encoding="utf-8-sig")

    val = next(item for item in split_summaries if item["split"] == "val")
    test = next(item for item in split_summaries if item["split"] == "test")
    report = (
        "鱼类活跃状态识别评估摘要\n"
        "========================\n"
        f"输入标签: {args.activity_csv}\n"
        f"时间划分: train={args.train_ratio:.2f}, val={args.val_ratio:.2f}, test={1 - args.train_ratio - args.val_ratio:.2f}\n"
        f"训练段学习阈值: low <= {low_thr:.6f}, high >= {high_thr:.6f}\n"
        f"验证集准确率: {val['accuracy']:.4f}\n"
        f"验证集宏平均 F1: {val['macro_f1']:.4f}\n"
        f"测试集准确率: {test['accuracy']:.4f}\n"
        f"测试集宏平均 F1: {test['macro_f1']:.4f}\n"
        "\n"
        "说明: 本评估使用训练时间段学习速度阈值，在后续时间段评估 low/normal/high 三类活跃状态。\n"
        "该指标适合作为状态识别算法的单项识别准确率；YOLO 检测指标应单独报告。\n"
    )
    (out_dir / "evaluation_summary.txt").write_text(report, encoding="utf-8")

    print(report)
    print(f"[OK] results saved to {out_dir}")


if __name__ == "__main__":
    main()
