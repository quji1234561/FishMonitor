#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Evaluate YOLO-box + activity-model predictions against generated activity GT."""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import numpy as np
import pandas as pd


STATE_ID_TO_NAME = {
    0: "low_activity",
    1: "normal_activity",
    2: "high_activity",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate temporal activity predictions with GT boxes and states")
    parser.add_argument("--gt-csv", required=True, help="activity_state_test.csv or activity_state_all.csv")
    parser.add_argument("--pred-csv", required=True, help="temporal_results.csv generated by analyze_temporal_states.py")
    parser.add_argument("--out-dir", required=True, help="Output evaluation directory")
    parser.add_argument("--gt-split", default="test", help="GT split to evaluate when the CSV contains a split column")
    parser.add_argument("--pred-state-col", default="smooth_state_id", help="Prediction state column")
    parser.add_argument("--gt-state-col", default="state_id", help="GT state column")
    parser.add_argument("--iou-thr", type=float, default=0.5, help="IoU threshold for a matched fish box")
    parser.add_argument("--pred-frame-offset", type=int, default=0, help="Add this offset to prediction frame ids")
    parser.add_argument("--unmatched-as-error", action="store_true", help="Count unmatched GT boxes as state errors")
    return parser.parse_args()


def ensure_xyxy(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    if {"x1", "y1", "x2", "y2"}.issubset(df.columns):
        return df
    required = {"x", "y", "w", "h"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"CSV missing box columns: {missing}")
    df["x1"] = df["x"]
    df["y1"] = df["y"]
    df["x2"] = df["x"] + df["w"]
    df["y2"] = df["y"] + df["h"]
    return df


def iou_matrix(gt_boxes: np.ndarray, pred_boxes: np.ndarray) -> np.ndarray:
    if len(gt_boxes) == 0 or len(pred_boxes) == 0:
        return np.zeros((len(gt_boxes), len(pred_boxes)), dtype=float)
    gx1, gy1, gx2, gy2 = [gt_boxes[:, i][:, None] for i in range(4)]
    px1, py1, px2, py2 = [pred_boxes[:, i][None, :] for i in range(4)]
    ix1 = np.maximum(gx1, px1)
    iy1 = np.maximum(gy1, py1)
    ix2 = np.minimum(gx2, px2)
    iy2 = np.minimum(gy2, py2)
    iw = np.maximum(ix2 - ix1, 0.0)
    ih = np.maximum(iy2 - iy1, 0.0)
    inter = iw * ih
    gt_area = np.maximum(gx2 - gx1, 0.0) * np.maximum(gy2 - gy1, 0.0)
    pred_area = np.maximum(px2 - px1, 0.0) * np.maximum(py2 - py1, 0.0)
    union = gt_area + pred_area - inter
    return np.divide(inter, union, out=np.zeros_like(inter), where=union > 0)


def greedy_match_frame(gt_frame: pd.DataFrame, pred_frame: pd.DataFrame, iou_thr: float) -> list[tuple[int, int, float]]:
    gt_boxes = gt_frame[["x1", "y1", "x2", "y2"]].to_numpy(dtype=float)
    pred_boxes = pred_frame[["x1", "y1", "x2", "y2"]].to_numpy(dtype=float)
    mat = iou_matrix(gt_boxes, pred_boxes)
    matches: list[tuple[int, int, float]] = []
    used_gt: set[int] = set()
    used_pred: set[int] = set()
    while mat.size:
        flat_idx = int(np.argmax(mat))
        gt_idx, pred_idx = np.unravel_index(flat_idx, mat.shape)
        best_iou = float(mat[gt_idx, pred_idx])
        if best_iou < iou_thr:
            break
        if gt_idx not in used_gt and pred_idx not in used_pred:
            matches.append((gt_idx, pred_idx, best_iou))
            used_gt.add(gt_idx)
            used_pred.add(pred_idx)
        mat[gt_idx, :] = -1.0
        mat[:, pred_idx] = -1.0
    return matches


def confusion_matrix(y_true: list[int], y_pred: list[int]) -> list[list[int]]:
    mat = np.zeros((3, 3), dtype=int)
    for t, p in zip(y_true, y_pred):
        if 0 <= int(t) <= 2 and 0 <= int(p) <= 2:
            mat[int(t), int(p)] += 1
    return mat.tolist()


def macro_f1(y_true: list[int], y_pred: list[int]) -> float:
    y_t = np.asarray(y_true, dtype=int)
    y_p = np.asarray(y_pred, dtype=int)
    scores = []
    for cls in [0, 1, 2]:
        tp = float(((y_t == cls) & (y_p == cls)).sum())
        fp = float(((y_t != cls) & (y_p == cls)).sum())
        fn = float(((y_t == cls) & (y_p != cls)).sum())
        precision = tp / (tp + fp) if tp + fp > 0 else 0.0
        recall = tp / (tp + fn) if tp + fn > 0 else 0.0
        scores.append(2 * precision * recall / (precision + recall) if precision + recall > 0 else 0.0)
    return float(np.mean(scores))


def main() -> None:
    args = parse_args()
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    gt = ensure_xyxy(pd.read_csv(args.gt_csv))
    pred = ensure_xyxy(pd.read_csv(args.pred_csv))
    if "split" in gt.columns and args.gt_split:
        gt = gt[gt["split"].astype(str) == args.gt_split].copy()
    if args.pred_state_col not in pred.columns:
        raise ValueError(f"Prediction CSV missing state column: {args.pred_state_col}")
    if args.gt_state_col not in gt.columns:
        raise ValueError(f"GT CSV missing state column: {args.gt_state_col}")

    gt["frame"] = gt["frame"].astype(int)
    pred["frame"] = pred["frame"].astype(int) + int(args.pred_frame_offset)
    gt = gt.sort_values(["frame", "track_id"]).reset_index(drop=True)
    pred = pred.sort_values(["frame", "track_id"]).reset_index(drop=True)

    matched_rows = []
    y_true: list[int] = []
    y_pred: list[int] = []
    total_gt = int(len(gt))
    total_pred = int(len(pred))
    matched_box_count = 0
    matched_pred_indices: set[int] = set()

    pred_groups = {int(k): v.reset_index(drop=False) for k, v in pred.groupby("frame")}
    for frame, gt_frame_raw in gt.groupby("frame"):
        gt_frame = gt_frame_raw.reset_index(drop=False)
        pred_frame = pred_groups.get(int(frame), pd.DataFrame(columns=list(pred.columns) + ["index"]))
        matches = greedy_match_frame(gt_frame, pred_frame, args.iou_thr)
        matched_gt_local = set()
        for gt_i, pred_i, iou in matches:
            matched_box_count += 1
            gt_row = gt_frame.iloc[gt_i]
            pred_row = pred_frame.iloc[pred_i]
            gt_state = int(gt_row[args.gt_state_col])
            pred_state = int(pred_row[args.pred_state_col])
            y_true.append(gt_state)
            y_pred.append(pred_state)
            matched_gt_local.add(gt_i)
            matched_pred_indices.add(int(pred_row["index"]))
            matched_rows.append({
                "frame": int(frame),
                "gt_track_id": int(gt_row.get("track_id", -1)),
                "pred_track_id": int(pred_row.get("track_id", -1)),
                "iou": float(iou),
                "gt_state_id": gt_state,
                "gt_state_name": STATE_ID_TO_NAME.get(gt_state, str(gt_state)),
                "pred_state_id": pred_state,
                "pred_state_name": STATE_ID_TO_NAME.get(pred_state, str(pred_state)),
                "correct": int(gt_state == pred_state),
            })
        if args.unmatched_as_error:
            for gt_i, gt_row in gt_frame.iterrows():
                if int(gt_i) in matched_gt_local:
                    continue
                gt_state = int(gt_row[args.gt_state_col])
                y_true.append(gt_state)
                y_pred.append(-1)
                matched_rows.append({
                    "frame": int(frame),
                    "gt_track_id": int(gt_row.get("track_id", -1)),
                    "pred_track_id": -1,
                    "iou": 0.0,
                    "gt_state_id": gt_state,
                    "gt_state_name": STATE_ID_TO_NAME.get(gt_state, str(gt_state)),
                    "pred_state_id": -1,
                    "pred_state_name": "unmatched",
                    "correct": 0,
                })

    matched_df = pd.DataFrame(matched_rows)
    valid_pairs = [(t, p) for t, p in zip(y_true, y_pred) if p in [0, 1, 2]]
    if valid_pairs:
        y_true_valid = [t for t, _ in valid_pairs]
        y_pred_valid = [p for _, p in valid_pairs]
        accuracy = float(np.mean(np.asarray(y_true_valid) == np.asarray(y_pred_valid)))
        macro_f1_value = macro_f1(y_true_valid, y_pred_valid)
        conf_mat = confusion_matrix(y_true_valid, y_pred_valid)
    else:
        accuracy = 0.0
        macro_f1_value = 0.0
        conf_mat = [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

    metrics = {
        "gt_csv": args.gt_csv,
        "pred_csv": args.pred_csv,
        "gt_split": args.gt_split,
        "iou_threshold": args.iou_thr,
        "total_gt_boxes": total_gt,
        "total_pred_boxes": total_pred,
        "matched_boxes": int(matched_box_count),
        "unmatched_gt_boxes": int(total_gt - matched_box_count),
        "unmatched_pred_boxes": int(total_pred - len(matched_pred_indices)),
        "detection_match_recall": float(len(matched_pred_indices) / total_gt) if total_gt else 0.0,
        "detection_match_precision": float(len(matched_pred_indices) / total_pred) if total_pred else 0.0,
        "state_accuracy_on_matched_boxes": accuracy,
        "state_macro_f1_on_matched_boxes": macro_f1_value,
        "confusion_matrix_rows_gt_cols_pred": conf_mat,
        "state_names": STATE_ID_TO_NAME,
    }

    matched_df.to_csv(out_dir / "matched_activity_predictions.csv", index=False, encoding="utf-8-sig")
    pd.DataFrame(conf_mat, index=[STATE_ID_TO_NAME[i] for i in range(3)], columns=[STATE_ID_TO_NAME[i] for i in range(3)]).to_csv(
        out_dir / "confusion_matrix.csv",
        encoding="utf-8-sig",
    )
    (out_dir / "metrics.json").write_text(json.dumps(metrics, ensure_ascii=False, indent=2), encoding="utf-8")
    print(json.dumps(metrics, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
